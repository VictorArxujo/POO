public class Carro{

	int  placa = 0;
	String modelo = "";
	int chassi = 0;


	public void setPlaca(int placa){
		this.placa = placa;
	}
	public void setModelo(String modelo){
		this.modelo = modelo;
	}
	public void setChassi(int chassi){
		this.chassi = chassi;	
	}

	public int getPlaca(){
		return placa;
	}
	public String getModelo(){
		return modelo;
	}
	public int getChassi(){
		return chassi;	
	}
	
	

}//fim da classe